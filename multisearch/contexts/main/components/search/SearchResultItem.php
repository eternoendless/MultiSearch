<?php

class SearchResultItem {

  /////////////////////////////////////////////////////////////////////////////
  // PUBLIC ATTRIBUTES
  /////////////////////////////////////////////////////////////////////////////
  

  /////////////////////////////////////////////////////////////////////////////
  // PRIVATE ATTRIBUTES
  /////////////////////////////////////////////////////////////////////////////
  
  public $title;
  
  public $url;
  
  public $urlPreview;
  
  public $summary;

  /////////////////////////////////////////////////////////////////////////////
  // PUBLIC METHODS
  /////////////////////////////////////////////////////////////////////////////
  
  /**
   * 
   * @param array $itemData
   */
  public function __construct(array $itemData) {
    $this->title = ifExists($itemData, 'title');
    $this->url = ifExists($itemData, 'url');
    $this->urlPreview = ifExists($itemData, 'urlPreview');
    $this->summary = ifExists($itemData, 'summary');
  }

  /////////////////////////////////////////////////////////////////////////////
  // PRIVATE METHODS
  /////////////////////////////////////////////////////////////////////////////


}